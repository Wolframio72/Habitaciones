package nebrija;

public class Gato extends Animal{
	
	private int vidas;
	private int patas;
	
	
	
	public int getVidas() {
		
		
		return vidas;
		
	}
	
	public int getPatas() {
		
		return patas;
		
		
	}
	
	public void setVidas(int vid) {
		
		
		vidas = vid;
		
		
	}
	
	public void setPatas(int pat) {
		
		
		patas = pat;
		
		
	}
	

}
